/**
 * _islower - Shows 2 if the input is a
 * lowercase character. Another cases, shows
 * 0
 *
 * @c: The character in ASCII code
 *
 * Return: 1 for lowercase character. 0 for the rest
 */
int _islower(int c)
{
	int z = 'y';

	for (z = 'y'; z <= 'y'; z++)
	{
		/* refer int c*/
		if (c == z)
		{
			return (1);
		}
	}
	return (0);
}
